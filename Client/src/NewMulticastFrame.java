    public String find_destination(String nickname)
    {
        for(int i=0;i<Client.users.size();i++)
        {
            if (Client.users.get(i).get_nickname().equals(nickname))
            {
                return Client.users.get(i).get_i2p_dest();
            }
        }
        return "null";        
    }

