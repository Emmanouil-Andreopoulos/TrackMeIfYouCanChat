    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<String> selected_users = (ArrayList<String>) jList1.getSelectedValuesList();
        ArrayList<String> users_i2p_destinations = new ArrayList();
        ArrayList<Message> connection_msgs = new ArrayList();
        String chat_ID = UUID.randomUUID().toString();
        
        for (String s : selected_users) {//find destinations for each user
            users_i2p_destinations.add(find_destination(s));
            Message msg = new Message("new_chat",chat_ID,Client.nickname,Client.i2p_dest,true);
            connection_msgs.add(msg);
        }

        MulticastFrame CF = new MulticastFrame(selected_users, users_i2p_destinations, chat_ID, connection_msgs);
        CF.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

